package j10_MethodCreation;

public class C07_AddNumbers { // Class basi


    public static int sumNumberss(int number1, int number2){ //sayi donen bir metod yapmak isterem
        int sum=0;
        for (int i = number1; i <=number2 ; i++) {
            sum += i;
        }
        return  sum; // toplami donecek
    }















} // Class sonu
